public class Lagu {
    
    private String idLagu;
    private String namaLagu;
    private String alamatLagu;

    /**
     * @return the idLagu
     */
    public String getIdLagu() {
        return idLagu;
    }

    /**
     * @param idLagu the idLagu to set
     */
    public void setIdLagu(String idLagu) {
        this.idLagu = idLagu;
    }

    /**
     * @return the namaLagu
     */
    public String getNamaLagu() {
        return namaLagu;
    }

    /**
     * @param namaLagu the namaLagu to set
     */
    public void setNamaLagu(String namaLagu) {
        this.namaLagu = namaLagu;
    }

    /**
     * @return the alamatLagu
     */
    public String getAlamatLagu() {
        return alamatLagu;
    }

    /**
     * @param alamatLagu the alamatLagu to set
     */
    public void setAlamatLagu(String alamatLagu) {
        this.alamatLagu = alamatLagu;
    }

}
